queue = []
queue.append(1)  # Append 1 to the stack
print("stack:", queue)
queue.append(2)  # Append 2 to the stack
print("stack:", queue)
print("pop 1st value:", queue.pop())  # Pop the first value (2) from the stack
print("pop 2nd value:", queue.pop())  # Pop the second value (1) from the stack
print("stack:", queue)
